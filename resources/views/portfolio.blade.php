  <script>
    document.getElementById("language-toggle").addEventListener("click", function() {
      let flagIcon = document.getElementById("flag-icon");
      let langText = document.getElementById("language-text");
      let currentLang = flagIcon.getAttribute("alt");

      if (currentLang === "Indonesia") {
        flagIcon.src = "assets/img/flag-us-svgrepo-com.svg";
        flagIcon.alt = "English";
        langText.textContent = "English";
        changeLanguage("en");
      } else {
        flagIcon.src = "assets/img/flag-id-svgrepo-com.svg";
        flagIcon.alt = "Indonesia";
        langText.textContent = "Bahasa";
        changeLanguage("id");
      }
    });

    function changeLanguage(lang) {
      document.querySelectorAll("[data-lang]").forEach(element => {
        if (element.getAttribute("data-lang") === lang) {
          element.classList.remove("d-none");
        } else {
          element.classList.add("d-none");
        }
      });
    }
  </script>

